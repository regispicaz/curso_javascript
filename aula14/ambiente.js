//Variável
var numb = 150;

//Neságem de Início da contagem
console.log('Vai começar...')

//Extrutura de repetição "for"
for(var cont = 1; cont <= numb; cont ++) {
    console.log(`Passo ${cont}`)//${} placeholder
}
//Menságem apresentada pós saída do bloco "for"
console.log('FIM!')
